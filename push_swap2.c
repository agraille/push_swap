#include "push_swap.h"

int	is_sorted(t_stack *a)
{
	int	i;

	i = 0;
	while (i < a->top)
	{
		if (a->data[i] < a->data[i + 1])
		{
			return (0);
		}
		i++;
	}
	return (1);
}
